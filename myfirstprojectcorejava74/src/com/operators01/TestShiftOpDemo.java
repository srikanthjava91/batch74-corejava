package com.operators01;

public class TestShiftOpDemo {

	public static void main(String[] args) {

		int a = 67;
		int b = 3;

		System.out.println(a << b);//LEFT SHIFT Operator 536
//		System.out.println(a <<< b); //Syntax error on token "<", delete this token
		
		System.out.println(a >> b);//LEFT SHIFT Operator 8
		System.out.println(a >>> b);//LEFT SHIFT Operator 8

	}

}
